template <typename Dtype>
void im2col_cpu(const Dtype* data_im, const int channels,
    const int height, const int width, const int kernel_h, const int kernel_w,
    const int pad_h, const int pad_w,
    const int stride_h, const int stride_w,
    Dtype* data_col) {
  int height_col = (height + 2 * pad_h - kernel_h) / stride_h + 1;
  int width_col = (width + 2 * pad_w - kernel_w) / stride_w + 1;
  int channels_col = channels * kernel_h * kernel_w;
  for (int c = 0; c < channels_col; ++c) {
    int w_offset = c % kernel_w;
    int h_offset = (c / kernel_w) % kernel_h;
    int c_im = c / kernel_h / kernel_w;
    for (int h = 0; h < height_col; ++h) {
      for (int w = 0; w < width_col; ++w) {
        int h_pad = h * stride_h - pad_h + h_offset;
        int w_pad = w * stride_w - pad_w + w_offset;
        if (h_pad >= 0 && h_pad < height && w_pad >= 0 && w_pad < width)
          data_col[(c * height_col + h) * width_col + w] =
            data_im[(c_im * height + h_pad) * width + w_pad];
        else
          data_col[(c * height_col + h) * width_col + w] = 0;
      }
    }
  }
}

template <typename Dtype>
void sparse_im2col_cpu(const Dtype* data_im, const int channels,
    const int height, const int width, const int kernel_h, const int kernel_w,
    const int pad_h, const int pad_w,
    const int stride_h, const int stride_w,
    Dtype* data_col) {
  int height_col = (height + 2 * pad_h - kernel_h) / stride_h + 1;
  int width_col = (width + 2 * pad_w - kernel_w) / stride_w + 1;
  int count = 2 * (kernel_w + kernel_h - 2) + 1;
  int channels_col = channels * count;
  int w_offset=0, h_offset=0;
  for (int c = 0; c < channels_col; ++c) {
    int id=c % count;
    int c_im = c / count;
    if(c%count==0) {
      w_offset=0;
      h_offset=0;
    }
    if (id>=0 && id<kernel_w-1) { //stage 1
      w_offset++;
    } else if (id>=kernel_w-1 && id<kernel_w+kernel_h-2) { //stage 2
      h_offset++;
    } else if (id>=kernel_w+kernel_h-2 && id<2*kernel_w+kernel_h-3) { //stage 3
      w_offset--;
    } else if (id>=2*kernel_w+kernel_h-3 && id<2*kernel_w+2*kernel_h-4) { //stage 4
      h_offset--;
    } else {
      h_offset=kernel_h / 2;
      w_offset=kernel_w / 2;
    }
    for (int h = 0; h < height_col; ++h) {
      for (int w = 0; w < width_col; ++w) {
        int h_pad = h * stride_h - pad_h + h_offset;
        int w_pad = w * stride_w - pad_w + w_offset;
        if (h_pad >= 0 && h_pad < height && w_pad >= 0 && w_pad < width)
          data_col[(c * height_col + h) * width_col + w] =
            data_im[(c_im * height + h_pad) * width + w_pad];
        else
          data_col[(c * height_col + h) * width_col + w] = 0;
      }
    }
  }
}

template <typename Dtype>
void more_sparse_im2col_cpu(const Dtype* data_im, const int channels,
    const int height, const int width, const int kernel_h, const int kernel_w,
    const int pad_h, const int pad_w,
    const int stride_h, const int stride_w,
    Dtype* data_col) {
  int height_col = (height + 2 * pad_h - kernel_h) / stride_h + 1;
  int width_col = (width + 2 * pad_w - kernel_w) / stride_w + 1;
  const int count=5;
  int channels_col = channels * count;
  int w_offset=0, h_offset=0;
  for (int c = 0; c < channels_col; ++c) {
    int id=c % count;
    int c_im = c / count;
    switch (id) {
      case 0:
        w_offset=0;
        h_offset=0;
        break;
      case 1:
        w_offset=kernel_w-1;
        h_offset=0;
        break;
      case 2:
        w_offset=kernel_w-1;
        h_offset=kernel_h-1;
        break;
      case 3:
        w_offset=0;
        h_offset=kernel_h-1;
        break;
      case 4:
        w_offset=kernel_w/2;
        h_offset=kernel_h/2;
        break;
      default:
        CHECK(false);
    }
    for (int h = 0; h < height_col; ++h) {
      for (int w = 0; w < width_col; ++w) {
        int h_pad = h * stride_h - pad_h + h_offset;
        int w_pad = w * stride_w - pad_w + w_offset;
        if (h_pad >= 0 && h_pad < height && w_pad >= 0 && w_pad < width)
          data_col[(c * height_col + h) * width_col + w] =
            data_im[(c_im * height + h_pad) * width + w_pad];
        else
          data_col[(c * height_col + h) * width_col + w] = 0;
      }
    }
  }
}

